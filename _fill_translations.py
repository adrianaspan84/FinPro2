def po_escape(s: str) -> str:
    return s.replace('\\', '\\\\').replace('"', '\\"').replace('\n', '\\n')


def fill_po(po_path: pathlib.Path, translations: dict):
    """Parse .po file and fill empty msgstr lines from translations dict."""
    content = po_path.read_text(encoding='utf-8')
    lines = content.splitlines(keepends=True)
    out = []
    i = 0
    while i < len(lines):
        line = lines[i]
        # Detect start of msgid (not the empty header one)
        if line.startswith('msgid "') and line.strip() != 'msgid ""':
            # Collect full msgid (may span multiple continuation lines)
            msgid_parts = [line[7:].rstrip('\n').rstrip('\r')]
            if msgid_parts[0].endswith('"'):
                msgid_parts[0] = msgid_parts[0][:-1]
            else:
                msgid_parts[0] = ''  # multiline start

            j = i + 1
            while j < len(lines) and lines[j].startswith('"'):
                chunk = lines[j].strip().strip('"')
                msgid_parts.append(chunk)
                j += 1

            raw_msgid = ''.join(msgid_parts).replace('\\"', '"').replace('\\n', '\n')

            out.append(line)
            for k in range(i + 1, j):
                out.append(lines[k])
            i = j

            # Skip blank lines between msgid and msgstr
            while i < len(lines) and lines[i].strip() == '':
                out.append(lines[i])
                i += 1

            # Now handle msgstr
            if i < len(lines) and lines[i].startswith('msgstr ""'):
                tr = translations.get(raw_msgid, '')
                if tr:
                    out.append(f'msgstr "{po_escape(tr)}"\n')
                else:
                    out.append(lines[i])
                i += 1
            continue
        out.append(line)
        i += 1

    po_path.write_text(''.join(out), encoding='utf-8')
    print(f"  Filled: {po_path} ({len(translations)} translations available)")


